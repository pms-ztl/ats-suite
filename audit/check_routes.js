const fs = require('fs');
const routesData = JSON.parse(fs.readFileSync('D:/CDC/ATS/audit/actual_routes.json', 'utf8'));

function makePattern(path) {
  return '^' + path.replace(/\//g, '\\/').replace(/:([^/]+)/g, '[^\\/]+') + '$';
}
const backendPatterns = routesData.routes.map(r => ({
  method: r.method,
  re: new RegExp(makePattern(r.path))
}));
function matchesBackend(method, clientPath) {
  const fullPath = '/api' + clientPath;
  return backendPatterns.some(bp => bp.method === method && bp.re.test(fullPath));
}

